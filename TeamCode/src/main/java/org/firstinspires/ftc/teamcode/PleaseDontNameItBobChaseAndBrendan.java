@Autonomous
public class PleaseDontNameItBobChaseAndBrendan extends OpMode {

    final double TICKS_PER_FOOT = 751.8;
    final double DISTANCE = 1 * TICKS_PER_FOOT;

    DcMotor leftDrive;
    DcMotor rightDrive;

    @Override
    public void init() {
        leftDrive = hardwareMap.get(DcMotor.class, "leftDrive");
        rightDrive = hardwareMap.get(DcMotor.class, "rightDrive");

        rightDrive.setDirection(DcMotorSimple.Direction.REVERSE);

        leftDrive.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);
        rightDrive.setMode(DcMotor.RunMode.STOP_AND_RESET_ENCODER);

        leftDrive.setTargetPosition(0);
        rightDrive.setTargetPosition(0);

        leftDrive.setMode(DcMotor.RunMode.RUN_TO_POSITION);
        rightDrive.setMode(DcMotor.RunMode.RUN_TO_POSITION);

        leftDrive.setPower(0.5);
        rightDrive.setPower(0.5);
    }

    @Override
    public void start() {
        leftDrive.setTargetPosition((int) DISTANCE);
        rightDrive.setTargetPosition((int) DISTANCE);
    }

    @Override
    public void loop() {

    }
}
